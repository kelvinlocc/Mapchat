public class marker_dataModel {
    String username;
    String longitude;
    String latitude;
    String string_title;
    String string_body;
    String time;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getString_title() {
        return string_title;
    }

    public void setString_title(String string_title) {
        this.string_title = string_title;
    }

    public String getString_body() {
        return string_body;
    }

    public void setString_body(String string_body) {
        this.string_body = string_body;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

}
